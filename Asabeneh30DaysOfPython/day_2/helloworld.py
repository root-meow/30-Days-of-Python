# Checking data types.

print(type(10))                     # Integer. 
print(type(3.14))                   # Float
print(type(1 * 3j))                 # Complex number.
print(type('Kamikaze'))             # String.
print(type([1, 2, 3]))              # List. It is ordered and allows storage of different data type items.
print(type({'name' : 'Asabeneh'}))  # Dictionary. Unordered collection of data in a key-value pair format.
print(type({9.8, 3.14, 2.7}))       # Set. Similar to lists and tuples. Unlike them, it is not an ordered collectionof iteems. Stores only unique items.
print(type((9.8, 3.14, 2.7)))       # Tuple. The values in tuples are immutable, cannot be changed.
